package com.bootcamp.springmvc.bootcampmvc.dto;

public class PaisDTO {

	String id;
	
	String descripcion;
	
	public PaisDTO() {
	}	
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescripcion() {
		return descripcion;
	}

	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}	
}
